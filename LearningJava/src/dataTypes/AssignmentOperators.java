package dataTypes;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		/* Assignments OPerators are (=, +=, -=, /=, and %=)
		 *  a = 7 meaning a=7
		 *  a+=7 meaning a+7
		 *  a-=7 meaning a-7
		 *  a*=7 meaning a*7
		 *  a/=7 meaning a/7
		 *  a%=7 meaning a%7
		 *  
		 */
		int a = 87;
		System.out.println(a+=7);  //94
		System.out.println(a-=7);  //87
		System.out.println(a*=7);  //
		System.out.println(a/=7);  //74
		System.out.println(a%=7);  //74
		System.out.println(a+=7);  //74
		


	}

}
