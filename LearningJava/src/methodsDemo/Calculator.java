package methodsDemo;

public class Calculator {
	
	//Creating a Method
	public int doAdd(int a, int b) {
	
		int c= a+b;
		return c;
		
	}	
	
	//Creating a second Method
	public int doSub (int a, int b) {
		
		int c = a-b;
		return c;
			
	}
	
	public String concatStr (String a, String b) {
		String c= a+" "+b;
		return c;
	}
	
	

}
